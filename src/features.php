        <h1>How to Use EcoCartographer</h1>
        <h2>Step 1: Enter Locations</h2>
        <p> Enter your starting and ending location in the top right corner box under starting location, like Google Maps. Try to be as specific as possible.</p>
        <h2>Step 2: Choose Vehicle</h2>
        <p> There is a drop-down menu of possible vehicles. There is a limited number of vehicles, so if you cannot find your vehicle, try to find a vehicle that is similar. Values for drag coefficient and frontal surface area, and some masses and engine displacements will be updated when you change the vehicle selection.</p>
        <h2>Step 3: Press Navigate!</h2>
        <p> Watch the console on the side of the screen. Once the program has finished calculating the route, a box will appear on the side with the directions of navigation, and routes will be drawn on the map.</p>
